package java_training;

public class Logical_OR_AND {
			public static void main(String[] args) {
			int x=60;
			int y=70;
				
		if (x>40 && y>50 && x>55 && y>90)
			
			System.out.println("Let's Play Football");
		else
			System.out.println("Let's Play Cricket");
					
		
		if (x>70 || y>75 || x>90 || y>65 )
			
			System.out.println("Let's Play Hockey");
		else
			System.out.println("Let's Play Tennis");

			}
}